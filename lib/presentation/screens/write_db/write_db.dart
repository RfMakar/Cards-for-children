import 'package:flutter/material.dart';

class WriteDataToFbScreen extends StatelessWidget {
  const WriteDataToFbScreen({super.key});
  final urlFb =
      'https://firebasestorage.googleapis.com/v0/b/cards-for-children.appspot.com/o';

  void loadTest() async {
    final storageRef = FirebaseStorage.instance.ref();
    final imageUrl =
        await storageRef.child("category/icon/0.png").getDownloadURL();
    print(imageUrl);
  }

  Future<void> _createDb() async {
    final firebaseFirestore = FirebaseFirestore.instance;

    for (int x = 0; x <= 22; x++) {
      final docRef = firebaseFirestore
          .collection("category")
          .withConverter(
            fromFirestore: CategoryCardModel.fromFirestore,
            toFirestore: (CategoryCardModel categoryCardModel, _) =>
                categoryCardModel.toFirestore(),
          )
          .doc('$x');
      final cat = CategoryCardModel(
        id: x,
        name: category[x]!,
        color: color[x]!,
        icon: '$urlFb/category%2Ficon%2F$x.png?alt=media',
        audio: '$urlFb/category%2Faudio%2F$x.mp3?alt=media',
      );
      await docRef.set(cat);
    }
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
        body: Center(
      child: Column(
        mainAxisAlignment: MainAxisAlignment.center,
        children: [
          ElevatedButton(
            onPressed: _createDb,
            child: const Text('write to collection Category'),
          ),
        ],
      ),
    ));
  }
}

